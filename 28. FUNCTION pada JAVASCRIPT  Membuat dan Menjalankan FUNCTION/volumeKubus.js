// Case menghitung volume 2 buah kubus.
// 1. Ketahui sisi masing" kubus
var kubusA = 8;
var kubusB = 3;
var volumeA;
var volumeB;
var total;
// 2. Hitung volume masing" kubus.
volumeA = kubusA * kubusA * kubusA;
volumeB = kubusB * kubusB * kubusB;
// 3. Jumlahkan hasilnya
total = volumeA + volumeB;
// 4. Kembalikan nilai jawabannya
console.log(total);
