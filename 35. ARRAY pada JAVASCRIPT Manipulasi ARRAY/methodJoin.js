// Method Array => Join
var myArr = ['Muhamad', 'Iqbal', 'Aditya'];
// console.log(myArr.join(' '));
// join() <== dalam parentheses bisa diisi apapun untuk menjadi jarak atau pemisah
myArr.push('Putra'); // push menambah elemen array di akhir arraynya.
myArr.pop(); // pop menghilangkan elemen terakhir dari sebuah array
myArr.unshift('Metaverse'); // unshift menambahkan elemen baru di awal array
myArr.shift(); //shift menghilangkan elemen pertama
console.log(myArr.join(' '));
